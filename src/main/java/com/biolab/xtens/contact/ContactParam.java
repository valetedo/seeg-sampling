package com.biolab.xtens.contact;

public class ContactParam {

	private String label;
	private String anatomical_area;
	private String epileptic_contact;
	private String bad_contact;
	private Float tp_dist;
	private Float x;
	private Float y;
	private Float z;
	
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getAnatomical_area() {
		return anatomical_area;
	}
	public void setAnatomical_area(String anatomical_area) {
		this.anatomical_area = anatomical_area;
	}
	public String getEpileptic_contact() {
		return epileptic_contact;
	}
	public void setEpileptic_contact(String epileptic_contact) {
		this.epileptic_contact = epileptic_contact;
	}
	public String getBad_contact() {
		return bad_contact;
	}
	public void setBad_contact(String bad_contact) {
		this.bad_contact = bad_contact;
	}
	public Float getTp_dist() {
		return tp_dist;
	}
	public void setTp_dist(Float tp_dist) {
		this.tp_dist = tp_dist;
	}
	public Float getX() {
		return x;
	}
	public void setX(Float x) {
		this.x = x;
	}
	public Float getY() {
		return y;
	}
	public void setY(Float y) {
		this.y = y;
	}
	public Float getZ() {
		return z;
	}
	public void setZ(Float z) {
		this.z = z;
	}
	
}